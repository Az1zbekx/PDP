#include <bits/stdc++.h>
using namespace std;
int main(){
    int i = 1;
    while(i <= 20){
        cout << i << " ";
        i++;
    }
    cout << endl;

    i = 25;
    while(i <= 100){
        cout << i << " ";
        i++;
    }
    cout << endl;

    i = 12;
    while(i <= 200){
        cout << i << " ";
        i++;
    }
    cout << endl;

    i = 15;
    while(i >= 2){
        cout << i << " ";
        i--;
    }
    cout << endl;

    i = 120;
    while(i >= 23){
        cout << i << " ";
        i--;
    }
    cout << endl;
    return 0;
}