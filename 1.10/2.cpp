#include <bits/stdc++.h>
using namespace std;
int main(){
    int i = 11;
    while(i <= 50){
        cout << i << " ";
        i += 2; 
    }
    cout << endl;

    i = 120;
    while(i <= 250){
        cout << i << " ";
        i += 2;
    }
    cout << endl;

    i = 99;
    while(i >= 50){
        cout << i << " ";
        i -= 2;
    }
    cout << endl;

    i = 80;
    while(i >= 20){
        cout << i << " ";
        i -= 2;
    }
    cout << endl;
    return 0;
}